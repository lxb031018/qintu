// ============================================
// 应用字体大小常量
//
// 统一定义应用中使用的字体大小
// 便于维护和保持 UI 一致性
// ============================================

class AppFontSizes {
  // ==================== 字体大小值 ====================

  /// 极小字体（9px，用于注释、辅助信息）
  static const double xsmall = 9.0;

  /// 小字体（10px，用于标签、徽章、时间戳）
  static const double small = 10.0;

  /// 较小字体（11px，用于状态标签）
  static const double smallMedium = 11.0;

  /// 说明文字（12px，用于提示、副标题）
  static const double caption = 12.0;

  /// 正文小（13px，用于次要正文）
  static const double bodySmall = 13.0;

  /// 标准正文（14px，用于常规文本）
  static const double body = 14.0;

  /// 大正文（15px，用于强调文本）
  static const double bodyLarge = 15.0;

  /// 标题小（16px，用于卡片标题、列表项）
  static const double titleSmall = 16.0;

  /// 标准标题（18px，用于页面标题）
  static const double title = 18.0;

  /// 大标题（20px，用于重要标题）
  static const double titleLarge = 20.0;

  /// 超大标题（24px，用于首页标题）
  static const double headline = 24.0;

  /// 特大标题（32px，用于欢迎页）
  static const double display = 32.0;
}
