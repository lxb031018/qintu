-- 设置字符集
SET NAMES utf8mb4;

-- ============================================================
-- 迁移 1: 修改 bind_code 字段为可空
-- 原因: 手机号绑定流程不再使用绑定码
-- ============================================================

-- 1. 删除 bind_code 的唯一索引
ALTER TABLE `user_bindings` DROP INDEX `uk_bind_code`;

-- 2. 修改 bind_code 字段为 DEFAULT NULL
ALTER TABLE `user_bindings` 
    MODIFY COLUMN `bind_code` VARCHAR(8) DEFAULT NULL 
    COMMENT '绑定码（已废弃，仅历史数据使用）';

-- 3. 添加 created_at 索引（如果不存在）
-- MySQL 8.0+ 支持 IF NOT EXISTS
ALTER TABLE `user_bindings` 
    ADD INDEX `idx_created_at` (`created_at`);

-- ============================================================
-- 迁移 2: 添加 pending 请求自动清理注释
-- 原因: 需要在代码层面实现自动过期逻辑
-- ============================================================

-- 为 expired_at 字段添加注释说明
-- 注意：此字段现在用于 pending 请求的过期时间
ALTER TABLE `user_bindings` 
    MODIFY COLUMN `expired_at` TIMESTAMP NULL DEFAULT NULL 
    COMMENT '过期时间（用于 pending 请求自动过期）';

